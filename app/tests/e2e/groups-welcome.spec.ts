test.describe.serial('Group-invite welcome screen — first-time visitor', () => {
  const GROUP_NAME = 'Welcome Screen Test Group';
  const INVITEE_NAME = 'Wanda Welcome';
  let ctxAdmin: BrowserContext;
  let ctxInvitee: BrowserContext;
  let pageAdmin: Page;
  let pageInvitee: Page;
  let inviteUrl = '';

  test.beforeAll(async ({ browser }) => {
    await computeTestKeypairs();
    ({ context: ctxAdmin, page: pageAdmin } = await bootUser(browser, USER_A, { nickname: 'Admin' }));
    ({ context: ctxInvitee, page: pageInvitee } = await bootAnonymous(browser));
  });

  test.afterAll(async () => {
    await ctxAdmin?.close();
    await ctxInvitee?.close();
  });

  test('Admin creates a group and generates a real invite link', async () => {
    await createGroup(pageAdmin, GROUP_NAME);
    inviteUrl = await generateInviteLink(pageAdmin, GROUP_NAME);
    await expect(pageAdmin.getByTestId('group-detail-page')).toBeVisible();
  });

  test('AC-GROUP-1/2/3, AC-PRIV-1/2: a fresh first-timer sees the blended welcome screen (not the bare join card) with the exact group name from the URL, gated on a name, and no public/kind-0 publish while merely rendering', async () => {
    // AC-PRIV-1/2: track every outgoing WS frame across the whole flow
    // (render through submit) and assert no kind-0 (public profile) event is
    // EVER published. Mirrors add-welcome.spec.ts's identical pattern: the
    // app legitimately opens relay connections for unrelated reasons (e.g. a
    // KeyPackage publish) on every identity mount, so "zero connections" is
    // not the invariant — "never a kind-0 EVENT" is.
    let kind0EventPublished = false;
    pageInvitee.on('websocket', (ws) => {
      ws.on('framesent', (frame) => {
        try {
          const text = typeof frame.payload === 'string' ? frame.payload : frame.payload.toString('utf-8');
          const data: unknown = JSON.parse(text);
          if (Array.isArray(data) && data[0] === 'EVENT' && (data[1] as { kind?: number })?.kind === 0) {
            kind0EventPublished = true;
          }
        } catch {
          // Non-JSON/non-EVENT frames (PING/AUTH/etc.) are irrelevant here.
        }
      });
    });

    const url = new URL(inviteUrl);
    const pathWithQuery = url.pathname + url.search;
    await pageInvitee.goto(pathWithQuery);
    await dismissErrorOverlay(pageInvitee);

    // AC-GROUP-1 / VQ-S4-007: the welcome screen renders, hosting the name
    // field + action inline — NOT the bare join card. Only one join-related
    // testid tree exists on the page at a time.
    await expect(pageInvitee.getByTestId('welcome-invite')).toBeVisible({ timeout: 30_000 });
    await expect(pageInvitee.getByTestId('join-request-card')).toHaveCount(0);

    // AC-GROUP-2: the invite line shows the EXACT group name from the URL's
    // name param (not a generic placeholder).
    await expect(pageInvitee.getByTestId('welcome-invite-line')).toHaveText(`You're invited to join ${GROUP_NAME}`);

    // AC-PRIV-2: rendering alone (a URL-only read, no relay query) cannot
    // have published anything by this point.
    expect(kind0EventPublished).toBe(false);

    // AC-GROUP-3 / VQ-S4-009: the join action is disabled until a non-blank
    // name, reactive to typing/clearing — the same shared predicate proven
    // for the contact variant (AC-CONTACT-5).
    const action = pageInvitee.getByTestId('welcome-primary-action');
    await expect(action).toBeDisabled();
    const nameInput = pageInvitee.getByTestId('welcome-name-input');
    await nameInput.fill(INVITEE_NAME);
    await expect(action).toBeEnabled();
    await nameInput.fill('');
    await expect(action).toBeDisabled();
    await nameInput.fill(INVITEE_NAME);
    await expect(action).toBeEnabled();

    // AC-GROUP-4: pressing the action saves the name, sends the real join
    // request via the existing send path, then shows the existing
    // confirmation. The welcome screen itself is gone afterward — no
    // separate join card ever appears (AC-GROUP-1).
    await action.click();
    await expect(pageInvitee.getByTestId('join-request-sent')).toBeVisible({ timeout: 30_000 });
    await expect(pageInvitee.getByTestId('welcome-invite')).toHaveCount(0);
    await expect(pageInvitee.getByTestId('join-request-card')).toHaveCount(0);

    // AC-NAME-1: the entered name persisted to the local profile (observable
    // state, not just the confirmation UI).
    await expect.poll(() => readSavedNickname(pageInvitee)).toBe(INVITEE_NAME);

    // AC-PRIV-1: across the FULL flow — render, name entry, saveProfile, and
    // the gift-wrapped join request — no public profile broadcast was ever
    // sent.
    expect(kind0EventPublished).toBe(false);
  });

  test("Admin observes the incoming join request with the newcomer's real name — proves a real gift-wrapped request was sent, not a UI-only confirmation (VQ-S4-010)", async () => {
    await pageAdmin.goto('/groups/');
    await expect(
      pageAdmin.getByTestId('groups-empty-state').or(pageAdmin.getByTestId('groups-list')),
    ).toBeVisible({ timeout: 60_000 });

    await expect
      .poll(
        async () => {
          await dismissErrorOverlay(pageAdmin);
          const badge = pageAdmin.getByTestId('notification-badge').first();
          return badge.isVisible();
        },
        { timeout: 60_000, intervals: [3_000, 5_000, 5_000, 5_000, 5_000, 5_000, 5_000] },
      )
      .toBe(true);

    await openGroupDetail(pageAdmin, GROUP_NAME);
    await expect(
      pageAdmin.locator('[data-testid^="pending-request-row-"]').filter({ hasText: INVITEE_NAME }),
    ).toBeVisible({ timeout: 30_000 });
  });
});


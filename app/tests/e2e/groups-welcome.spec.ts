test.describe('Group-invite welcome screen — send failure surfaces the error Alert (VQ-S4-016)', () => {
  const GROUP_NAME = 'Welcome Screen Error-Path Group';
  const INVITEE_NAME = 'Erroring Erin';

  test('a fresh first-timer whose join request fails to publish sees the joinRequestError Alert above WelcomeInvite, and is not left stuck', async ({ browser }) => {
    await computeTestKeypairs();
    const { context: ctxAdmin, page: pageAdmin } = await bootUser(browser, USER_A, { nickname: 'Admin' });
    const { context: ctxInvitee, page: pageInvitee } = await bootAnonymous(browser);

    try {
      await createGroup(pageAdmin, GROUP_NAME);
      const inviteUrl = await generateInviteLink(pageAdmin, GROUP_NAME);

      const url = new URL(inviteUrl);
      const pathWithQuery = url.pathname + url.search;
      await pageInvitee.goto(pathWithQuery);
      await dismissErrorOverlay(pageInvitee);

      await expect(pageInvitee.getByTestId('welcome-invite')).toBeVisible({ timeout: 30_000 });
      const nameInput = pageInvitee.getByTestId('welcome-name-input');
      await nameInput.fill(INVITEE_NAME);
      const action = pageInvitee.getByTestId('welcome-primary-action');
      await expect(action).toBeEnabled();

      // Deterministically force sendJoinRequest's relay publish to fail: the
      // page/app is already fully loaded (identity + group data resolved
      // in memory), so cutting the context's network here only affects the
      // NEW WebSocket connections `rawPublish` opens per relay at click
      // time. Those either fail fast via `ws.onerror` or, worst case,
      // exhaust their own internal 10s per-relay timeout — either way a
      // deterministic failure, never a relay-availability-dependent flake.
      await ctxInvitee.setOffline(true);
      await action.click();

      // JoinRequestCard's welcome-variant branch renders a standalone
      // Chakra `Alert` (role="alert" by default) ABOVE `WelcomeInvite` when
      // the existing `error` state is set — not inside WelcomeInvite, which
      // has no error slot by design (S2).
      const wrapper = pageInvitee.getByTestId('join-request-welcome-wrapper');
      const errorAlert = wrapper.getByRole('alert');
      await expect(errorAlert).toBeVisible({ timeout: 15_000 });
      await expect(errorAlert).toContainText('Failed to send join request');

      // Not a dead end: the welcome screen is still there and the action is
      // clickable again — `sending` is reset in handleSendRequest's
      // `finally` branch — and no false "sent" confirmation ever appeared.
      await expect(pageInvitee.getByTestId('welcome-invite')).toBeVisible();
      await expect(action).toBeEnabled();
      await expect(pageInvitee.getByTestId('join-request-sent')).toHaveCount(0);
    } finally {
      await ctxInvitee.setOffline(false).catch(() => {});
      await ctxAdmin.close();
      await ctxInvitee.close();
    }
  });
});

